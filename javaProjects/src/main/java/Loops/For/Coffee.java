package Loops.For;

class Coffee {
//Refactoring a while loop into a For-Loop
    public static void main(String[] args) {

        for(int cupsOfCoffee=1; cupsOfCoffee <= 100; cupsOfCoffee++){
            System.out.println("Fry drinks cup of coffee #" + cupsOfCoffee);
        }
    }
}
